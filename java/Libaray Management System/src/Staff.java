public class Staff{
    private String Staff_name;
    private String Staff_phoneNumber;
    private String Staff_registrationNumber;
    private String Staff_address;

    public void setStaff_Name(String Staff_name) {
        this.Staff_name = Staff_name;

    }
    public void setStaff_PhoneNumber(String Staff_phoneNumber) {
        this.Staff_phoneNumber = Staff_phoneNumber;

    }
    public void setStaff_RegistrationNumber(String Staff_registrationNumber) {
        this.Staff_registrationNumber = Staff_registrationNumber;

    }
    public void setStaff_Address(String Staff_address) {
        this.Staff_address = Staff_address;
    }

    public String getStaffName() {
        return Staff_name;
    }

    public String getStaffPhoneNumber() {
        return Staff_phoneNumber;
    }

    public String getStaffRegistrationNumber() {
        return Staff_registrationNumber;
    }

    public String getStaffAddress() {
        return Staff_address;
    }

}
